package com.neusoft.lj.entity;

public class Supplier_Entity {

    private int supplierno;
    private String suppliername;
    private String supplierCEO;
    private String contact;
    private String tel;
    private String address;

    public int getSupplierno() {
        return supplierno;
    }

    public void setSupplierno(int supplierno) {
        this.supplierno = supplierno;
    }

    public String getSuppliername() {
        return suppliername;
    }

    public void setSuppliername(String suppliername) {
        this.suppliername = suppliername;
    }

    public String getSupplierCEO() {
        return supplierCEO;
    }

    public void setSupplierCEO(String supplierCEO) {
        this.supplierCEO = supplierCEO;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
